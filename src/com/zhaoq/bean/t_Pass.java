package com.zhaoq.bean;

public class t_Pass {
	//用户编号:User varchar(32),用户姓名：Name varchar(32),密码：Pass varchar(32)
	private String Name;
	private String Pass;
	private String User;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getPass() {
		return Pass;
	}
	public void setPass(String pass) {
		Pass = pass;
	}
	public String getUser() {
		return User;
	}
	public void setUser(String user) {
		User = user;
	}


}
